public class AssignmentOperator{

public static void main(String[] args) {
	int i=10,j=20;

	System.out.println("i="+i+"\nj="+j);
	System.out.println("i+=j : "+(i+=j));	
	System.out.println("i-=j : "+(i-=j));	
	System.out.println("i*=j : "+(i*=j));	
	System.out.println("i/=j : "+(i/=j));	
	System.out.println("i%=j : "+(i%=j));	
}
}